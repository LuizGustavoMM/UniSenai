public class ImparPar {
    public static void main(String[] args) {
    
        int x = 9;
        int y = 7;
        int z = x + y;
        if (z % 2 == 1) {
            System.out.println("O numero eh impar!\nNumero: " + z);
        } else {
            System.out.println("O numero eh par!\nNumero: " + z);
        }
    }

}
